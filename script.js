// Memory Lane Functions
function submitMemory() {
  const memoryInput = document.getElementById("memoryInput").value;
  if (memoryInput) {
    // Get existing memories from local storage
    const memories = JSON.parse(localStorage.getItem("memories")) || [];
    memories.push(memoryInput);
    localStorage.setItem("memories", JSON.stringify(memories)); // Save to local storage
    document.getElementById("memoryInput").value = ""; // Clear input
    displayMemories();
  }
}

function displayMemories() {
  const memories = JSON.parse(localStorage.getItem("memories")) || [];
  const memoryDisplay = document.getElementById("randomMemory");
  memoryDisplay.innerHTML = ""; // Clear previous memories
  memories.forEach((memory) => {
    const memoryElement = document.createElement("div");
    memoryElement.textContent = memory;
    memoryDisplay.appendChild(memoryElement);
  });
}

